function getDailyThought() {
  const today = new Date().toISOString().slice(0, 10);
  const seed = today.split('-').join('');
  const index = Number(seed) % THOUGHTS.length;
  return THOUGHTS[index];
}

function Positivity() {
  const [thought, setThought] = useState(getDailyThought());

  const handleNewThought = () => {
    const next = THOUGHTS[Math.floor(Math.random() * THOUGHTS.length)];
    setThought(next);
  };

  return (
    <section className="PositivityPage">
      <div className="PositivityHeader">
        <h1>Позитивни мисли</h1>
        <p>Дозволи една инспиративна мисла да го промени твојот ден.</p>
      </div>

      <div className="PositivityCard">
        <p className="PositivityQuote">“{thought}”</p>
        <button type="button" onClick={handleNewThought}>
          Нова позитивна мисла
        </button>
      </div>

      <div className="PositivityFooter">
        <p></p>
      </div>
    </section>
  );
}

